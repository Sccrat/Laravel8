    {
      $companyId = $request->input('company_id');
        $merged = MergedPosition::with('zone_position_from', 'zone_position_to')
                  ->whereHas('zone_position_from.zone.warehouse.distribution_center', function ($q) use ($companyId) {
                    $q->where('company_id', $companyId);
                  })
                  ->get();
        return $merged->toArray();
    }

